// Calcula la RMS de un array de floats
float calc_RMS(float *arr, int ventana){
    float raiz = 0;
    float sum = 0;

    for(int i = 0; i < ventana; i++){
        sum += arr[i] * arr[i];
    }

    raiz = sqrt(sum/(float)ventana);
    return raiz;
}

